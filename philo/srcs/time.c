#include "philosophers.h"

long	get_time(void)
{
	struct timeval	tv;

	gettimeofday(&tv, NULL);
	return ((tv.tv_sec * 1000) + (tv.tv_usec / 1000));
}

long	calcul_ms(t_infos *infos)
{
	return (get_time() - infos->ms_start);
}
